import contains_duplicate

def testcases(exp: str, s: contains_duplicate.Solution, nums: str):
    print("--------------- Teste iniciado ---------------")
    print("expected = " + str(exp) + "\noutput   = " + str(s.containsDuplicate(nums)))
    
    print("--------------- Teste finalizado ---------------")

s = contains_duplicate.Solution()

nums = [1,2,3,1]
testcases("True", s, nums)

nums = [1,2,3,4]
testcases("False", s, nums)

nums = [1,1,1,3,3,4,3,2,4,2]
testcases("True", s, nums)
